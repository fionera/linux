int choose_vol(unsigned int adecVolume, int low, int high)
{
    unsigned int middle_vol = (kcontrol_volume[high] - kcontrol_volume[low])/2;
    if(adecVolume > kcontrol_volume[low] + middle_vol)
        return high;
    else
        return low;
}

unsigned int adecVolume_search(unsigned int adecVolume)
{
    int low_index = 0;
    int high_index = RTK_VOLUME_NUM - 1;

    while (low_index <= high_index){
        int middle = (low_index + high_index)/2;

        if (kcontrol_volume[middle] == adecVolume){
            pr_debug("[%s]index %d->0x%x (%s dB)\n",__FUNCTION__, middle, rtk_volume[middle], str_dB[middle]);
            return rtk_volume[middle];
        }

        if((high_index -1 == low_index) || (high_index == middle) || (low_index == middle)){
            pr_debug("[%s] index %d->0x%x (%s dB)\n",__FUNCTION__, choose_vol(adecVolume, low_index, high_index),
                        rtk_volume[choose_vol(adecVolume, low_index, high_index)],
                        str_dB[choose_vol(adecVolume, low_index, high_index)]);
            return rtk_volume[choose_vol(adecVolume, low_index, high_index)];
        }

        if (kcontrol_volume[middle] > adecVolume)
            high_index = middle - 1;
        else
            low_index = middle + 1;
    }

    pr_err("[%s] map to gain table fail!\n", __FUNCTION__);
    return (unsigned int)ALSA_GAIN_0dB; //return default value

}
